#include <msghandle/predef/detail/test.h>

#ifndef MSGHANDLE_PREDEF_MAKE_H
#define MSGHANDLE_PREDEF_MAKE_H

/*
Shorthands for the common version number formats used by vendors...
*/

/*`
[heading `MSGHANDLE_PREDEF_MAKE_..` macros]

These set of macros decompose common vendor version number
macros which are composed version, revision, and patch digits.
The naming convention indicates:

* The base of the specified version number. "`MSGHANDLE_PREDEF_MAKE_0X`" for
  hexadecimal digits, and "`MSGHANDLE_PREDEF_MAKE_10`" for decimal digits.
* The format of the vendor version number. Where "`V`" indicates the version digits,
  "`R`" indicates the revision digits, "`P`" indicates the patch digits, and "`0`"
  indicates an ignored digit.

Macros are:
*/
/*` `MSGHANDLE_PREDEF_MAKE_0X_VRP(V)` */
#define MSGHANDLE_PREDEF_MAKE_0X_VRP(V) MSGHANDLE_VERSION_NUMBER((V&0xF00)>>8,(V&0xF0)>>4,(V&0xF))
/*` `MSGHANDLE_PREDEF_MAKE_0X_VVRP(V)` */
#define MSGHANDLE_PREDEF_MAKE_0X_VVRP(V) MSGHANDLE_VERSION_NUMBER((V&0xFF00)>>8,(V&0xF0)>>4,(V&0xF))
/*` `MSGHANDLE_PREDEF_MAKE_0X_VRPP(V)` */
#define MSGHANDLE_PREDEF_MAKE_0X_VRPP(V) MSGHANDLE_VERSION_NUMBER((V&0xF000)>>12,(V&0xF00)>>8,(V&0xFF))
/*` `MSGHANDLE_PREDEF_MAKE_0X_VVRR(V)` */
#define MSGHANDLE_PREDEF_MAKE_0X_VVRR(V) MSGHANDLE_VERSION_NUMBER((V&0xFF00)>>8,(V&0xFF),0)
/*` `MSGHANDLE_PREDEF_MAKE_0X_VRRPPPP(V)` */
#define MSGHANDLE_PREDEF_MAKE_0X_VRRPPPP(V) MSGHANDLE_VERSION_NUMBER((V&0xF000000)>>24,(V&0xFF0000)>>16,(V&0xFFFF))
/*` `MSGHANDLE_PREDEF_MAKE_0X_VVRRP(V)` */
#define MSGHANDLE_PREDEF_MAKE_0X_VVRRP(V) MSGHANDLE_VERSION_NUMBER((V&0xFF000)>>12,(V&0xFF0)>>4,(V&0xF))
/*` `MSGHANDLE_PREDEF_MAKE_0X_VRRPP000(V)` */
#define MSGHANDLE_PREDEF_MAKE_0X_VRRPP000(V) MSGHANDLE_VERSION_NUMBER((V&0xF0000000)>>28,(V&0xFF00000)>>20,(V&0xFF000)>>12)
/*` `MSGHANDLE_PREDEF_MAKE_0X_VVRRPP(V)` */
#define MSGHANDLE_PREDEF_MAKE_0X_VVRRPP(V) MSGHANDLE_VERSION_NUMBER((V&0xFF0000)>>16,(V&0xFF00)>>8,(V&0xFF))
/*` `MSGHANDLE_PREDEF_MAKE_10_VPPP(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VPPP(V) MSGHANDLE_VERSION_NUMBER(((V)/1000)%10,0,(V)%1000)
/*` `MSGHANDLE_PREDEF_MAKE_10_VR0(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VR0(V) MSGHANDLE_VERSION_NUMBER(((V)/100)%10,((V)/10)%10,0)
/*` `MSGHANDLE_PREDEF_MAKE_10_VRP(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VRP(V) MSGHANDLE_VERSION_NUMBER(((V)/100)%10,((V)/10)%10,(V)%10)
/*` `MSGHANDLE_PREDEF_MAKE_10_VRP000(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VRP000(V) MSGHANDLE_VERSION_NUMBER(((V)/100000)%10,((V)/10000)%10,((V)/1000)%10)
/*` `MSGHANDLE_PREDEF_MAKE_10_VRPPPP(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VRPPPP(V) MSGHANDLE_VERSION_NUMBER(((V)/100000)%10,((V)/10000)%10,(V)%10000)
/*` `MSGHANDLE_PREDEF_MAKE_10_VRPP(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VRPP(V) MSGHANDLE_VERSION_NUMBER(((V)/1000)%10,((V)/100)%10,(V)%100)
/*` `MSGHANDLE_PREDEF_MAKE_10_VRR(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VRR(V) MSGHANDLE_VERSION_NUMBER(((V)/100)%10,(V)%100,0)
/*` `MSGHANDLE_PREDEF_MAKE_10_VRRPP(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VRRPP(V) MSGHANDLE_VERSION_NUMBER(((V)/10000)%10,((V)/100)%100,(V)%100)
/*` `MSGHANDLE_PREDEF_MAKE_10_VRR000(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VRR000(V) MSGHANDLE_VERSION_NUMBER(((V)/100000)%10,((V)/1000)%100,0)
/*` `MSGHANDLE_PREDEF_MAKE_10_VV00(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VV00(V) MSGHANDLE_VERSION_NUMBER(((V)/100)%100,0,0)
/*` `MSGHANDLE_PREDEF_MAKE_10_VVRR(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VVRR(V) MSGHANDLE_VERSION_NUMBER(((V)/100)%100,(V)%100,0)
/*` `MSGHANDLE_PREDEF_MAKE_10_VVRRP(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VVRRP(V) MSGHANDLE_VERSION_NUMBER(((V)/1000)%100,((V)/10)%100,(V)%10)
/*` `MSGHANDLE_PREDEF_MAKE_10_VVRRPP(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VVRRPP(V) MSGHANDLE_VERSION_NUMBER(((V)/10000)%100,((V)/100)%100,(V)%100)
/*` `MSGHANDLE_PREDEF_MAKE_10_VVRRPPP(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VVRRPPP(V) MSGHANDLE_VERSION_NUMBER(((V)/100000)%100,((V)/1000)%100,(V)%1000)
/*` `MSGHANDLE_PREDEF_MAKE_10_VVRR0PP00(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VVRR0PP00(V) MSGHANDLE_VERSION_NUMBER(((V)/10000000)%100,((V)/100000)%100,((V)/100)%100)
/*` `MSGHANDLE_PREDEF_MAKE_10_VVRR0PPPP(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VVRR0PPPP(V) MSGHANDLE_VERSION_NUMBER(((V)/10000000)%100,((V)/100000)%100,(V)%10000)
/*` `MSGHANDLE_PREDEF_MAKE_10_VVRR00PP00(V)` */
#define MSGHANDLE_PREDEF_MAKE_10_VVRR00PP00(V) MSGHANDLE_VERSION_NUMBER(((V)/100000000)%100,((V)/1000000)%100,((V)/100)%100)
/*`
[heading `MSGHANDLE_PREDEF_MAKE_*..` date macros]

Date decomposition macros return a date in the relative to the 1970
Epoch date. If the month is not available, January 1st is used as the month and day.
If the day is not available, but the month is, the 1st of the month is used as the day.
*/
/*` `MSGHANDLE_PREDEF_MAKE_DATE(Y,M,D)` */
#define MSGHANDLE_PREDEF_MAKE_DATE(Y,M,D) MSGHANDLE_VERSION_NUMBER((Y)%10000-1970,(M)%100,(D)%100)
/*` `MSGHANDLE_PREDEF_MAKE_YYYYMMDD(V)` */
#define MSGHANDLE_PREDEF_MAKE_YYYYMMDD(V) MSGHANDLE_PREDEF_MAKE_DATE(((V)/10000)%10000,((V)/100)%100,(V)%100)
/*` `MSGHANDLE_PREDEF_MAKE_YYYY(V)` */
#define MSGHANDLE_PREDEF_MAKE_YYYY(V) MSGHANDLE_PREDEF_MAKE_DATE(V,1,1)
/*` `MSGHANDLE_PREDEF_MAKE_YYYYMM(V)` */
#define MSGHANDLE_PREDEF_MAKE_YYYYMM(V) MSGHANDLE_PREDEF_MAKE_DATE((V)/100,(V)%100,1)

#endif
